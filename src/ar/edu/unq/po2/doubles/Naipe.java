package ar.edu.unq.po2.doubles;

public class Naipe {

	private Palo palo;
	private int valor;
	
	public Naipe(Palo palo, int valor) {
		super();
		this.palo = palo;
		this.valor = valor;
	}

	public Palo getPalo() {
		return palo;
	}

	public void setPalo(Palo palo) {
		this.palo = palo;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}
	
	
	
	
	
	
}
